    /**
     * Grids interpreter, builds based on sixGrid and winCondition
     * @return board
     */
    public Board sixPlayersGame() {
        Board board = new Board(sixGrid[0].length,sixGrid.length);
        for(int i=0;i<sixGrid.length;i++) {
            for(int j=0;j<sixGrid[i].length;j++) {
                int number = sixGrid[i][j];
                if(number == 0) {
                    board.Grid[j][i].setActive(false);
                }else if(number == 1) {
                    board.Grid[j][i].setActive(true);
                }else {
                    board.insertPawn(j, i, number-2);
                }
            }
        }
        board.winCondition = this.getWinCondition();
        return board;
    }

    /**
     * returns winCondition array
     */
    public int[][] getWinCondition() {
        return winCondition;
    }

